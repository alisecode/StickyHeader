import SwiftUI

extension Color {
    
    static var systemBlack: Color {
        Color(UIColor(red: 4/255, green: 4/255, blue: 4/255, alpha: 1))
    }
    
    static var systemGray: Color {
        Color(UIColor(red: 179/255, green: 179/255, blue: 179/255, alpha: 1))
    }
    
    static var artistBackground: Color {
        Color(UIColor(red: 96/255, green: 96/255, blue: 96/255, alpha: 1))
    }
}
